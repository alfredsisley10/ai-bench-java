    static String onboardingFlow(String pkg, String cls, String foundation, BrandSpec b) {
        return """
                package %PKG%;

                import java.util.ArrayList;
                import java.util.List;

                /**
                 * Brand-tailored onboarding flow for %FOUND% under %DISPLAY%.
                 * Step ordering and which steps are required differ by brand:
                 * LegacyTrust adds an in-person advisor intro; OmniDirect skips
                 * physical mail; FutureBank requires a parent account.
                 */
                public final class %CLS%OnboardingFlow {

                    public record Step(String code, String label, boolean required, int sequenceNumber) {}

                    public List<Step> steps() {
                        List<Step> steps = new ArrayList<>();
                        int seq = 0;
                        steps.add(new Step("IDENTITY_CAPTURE", "Verify your identity", true, seq++));
                        steps.add(new Step("ADDRESS_CONFIRM", "Confirm your address", true, seq++));
                        steps.add(new Step("TERMS_ACCEPT", "Accept account agreement", true, seq++));

                        if ("FUTUREBANK".equals("%CODE%")) {
                            steps.add(new Step("PARENT_GUARDIAN_VERIFY",
                                    "Parent or guardian verification", true, seq++));
                        }

                        if ("LEGACYTRUST".equals("%CODE%")) {
                            steps.add(new Step("WEALTH_SOURCE_DISCLOSURE",
                                    "Source-of-wealth disclosure (private client)", true, seq++));
                            steps.add(new Step("RELATIONSHIP_MANAGER_INTRO",
                                    "Meet your relationship manager", true, seq++));
                        }

                        if ("STARTUPFI".equals("%CODE%")) {
                            steps.add(new Step("BUSINESS_FORMATION_DOCS",
                                    "Upload formation documents", true, seq++));
                            steps.add(new Step("BENEFICIAL_OWNERS",
                                    "Declare 25%+ beneficial owners", true, seq++));
                        }

                        steps.add(new Step("FUNDING_INTENT",
                                "Choose initial funding method", true, seq++));

                        if (!%DIGITAL%) {
                            steps.add(new Step("PHYSICAL_DELIVERY",
                                    "Shipping address for cards and debit cards", true, seq++));
                        } else {
                            steps.add(new Step("VIRTUAL_CARD_ISSUANCE",
                                    "Issue virtual card immediately", true, seq++));
                        }

                        if ("OMNIDIRECT".equals("%CODE%")) {
                            steps.add(new Step("APP_INSTALL_PROMPT",
                                    "Install mobile app to continue", true, seq++));
                        }

                        steps.add(new Step("FINAL_REVIEW", "Review and confirm", true, seq++));
                        return steps;
                    }

                    public int stepCount() {
                        return steps().size();
                    }

                    public boolean hasStep(String code) {
                        return steps().stream().anyMatch(s -> s.code().equals(code));
                    }

                    public long estimatedMinutes() {
                        return switch ("%CODE%") {
                            case "OMNIDIRECT" -> 4;
                            case "OMNIBANK" -> 8;
                            case "LEGACYTRUST" -> 45;
                            case "STARTUPFI" -> 15;
                            case "SILVERBANK" -> 12;
                            case "FUTUREBANK" -> 10;
                            default -> 10;
                        };
                    }
                }
                """
                .replace("%PKG%", pkg)
                .replace("%CLS%", cls)
                .replace("%FOUND%", foundation)
                .replace("%CODE%", b.code())
                .replace("%DISPLAY%", b.displayName())
                .replace("%DIGITAL%", String.valueOf(b.digitalOnly()));
    }

    static String pricingOverride(String pkg, String cls, String foundation, BrandSpec b) {
        return """
                package %PKG%;

                import java.math.BigDecimal;
                import java.math.RoundingMode;

                /**
                 * Brand-specific pricing adjustments relative to the national
                 * posted rate. Digital-only brands bump rates to compete;
                 * premium brands take a service-fee premium; community banks
                 * offer relationship discounts.
                 */
                public final class %CLS%PricingOverride {

                    public BigDecimal apyBonusBps() {
                        return switch ("%CODE%") {
                            case "OMNIDIRECT" -> new BigDecimal("25");     // +0.25%
                            case "LEGACYTRUST" -> new BigDecimal("15");    // +0.15%
                            case "BLUEWATERBANK" -> new BigDecimal("10");  // +0.10%
                            case "FUTUREBANK" -> new BigDecimal("5");
                            case "STARTUPFI" -> new BigDecimal("20");
                            case "SILVERBANK" -> BigDecimal.ZERO;
                            default -> BigDecimal.ZERO;
                        };
                    }

                    public BigDecimal monthlyFeeAdjustment() {
                        return switch ("%CODE%") {
                            case "OMNIDIRECT" -> new BigDecimal("-5.00"); // no monthly fee at all
                            case "LEGACYTRUST" -> new BigDecimal("25.00"); // premium service fee
                            case "SILVERBANK" -> new BigDecimal("-3.00");
                            case "FUTUREBANK" -> new BigDecimal("-12.00"); // free for minors
                            default -> BigDecimal.ZERO;
                        };
                    }

                    public BigDecimal adjustApy(BigDecimal nationalApy) {
                        BigDecimal bpsAsRate = apyBonusBps()
                                .divide(BigDecimal.valueOf(10000), 6, RoundingMode.HALF_EVEN);
                        return nationalApy.add(bpsAsRate);
                    }

                    public BigDecimal adjustMonthlyFee(BigDecimal national) {
                        BigDecimal adj = national.add(monthlyFeeAdjustment());
                        return adj.signum() < 0 ? BigDecimal.ZERO : adj;
                    }

                    public boolean isPromotionalRateActive() {
                        return switch ("%CODE%") {
                            case "OMNIDIRECT", "STARTUPFI" -> true;
                            default -> false;
                        };
                    }
                }
                """
                .replace("%PKG%", pkg)
                .replace("%CLS%", cls)
                .replace("%FOUND%", foundation)
                .replace("%CODE%", b.code());
    }


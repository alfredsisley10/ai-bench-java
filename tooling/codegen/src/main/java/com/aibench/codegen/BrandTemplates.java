    static String packageInfo(String pkg, String foundation, BrandSpec b) {
        return """
                /**
                 * %FOUND% as offered under the %DISPLAY% brand.
                 *
                 * <p>Primary segment: %SEGMENT%<br>
                 * Digital-only: %DIGITAL%<br>
                 * Premium: %PREMIUM%<br>
                 * Founded: %FOUNDED%
                 *
                 * <p>Generated by {@code BrandVariantGenerator}. Do not hand-edit.
                 */
                package %PKG%;
                """
                .replace("%PKG%", pkg)
                .replace("%FOUND%", foundation)
                .replace("%DISPLAY%", b.displayName())
                .replace("%SEGMENT%", b.primarySegment())
                .replace("%DIGITAL%", String.valueOf(b.digitalOnly()))
                .replace("%PREMIUM%", String.valueOf(b.premiumBrand()))
                .replace("%FOUNDED%", String.valueOf(b.brandFounded()));
    }


    static String parser(String pkg, String cls, MtSpec s) {
        return """
                package %PKG%;

                import java.math.BigDecimal;
                import java.time.Instant;
                import java.time.LocalDate;
                import java.time.format.DateTimeFormatter;
                import java.util.ArrayList;
                import java.util.Currency;
                import java.util.HashMap;
                import java.util.List;
                import java.util.Map;
                import java.util.Objects;

                /**
                 * Parser for SWIFT %MT% messages in the block-4 tag format.
                 * This implementation understands the subset of tags typically
                 * present in a %MT%; unknown tags are preserved as raw strings.
                 */
                public final class %CLS%Parser {

                    private static final DateTimeFormatter DATE = DateTimeFormatter.ofPattern("yyMMdd");

                    public %CLS%Message parse(String rawBlock4) {
                        Objects.requireNonNull(rawBlock4, "rawBlock4");
                        Map<String, String> tags = tokenize(rawBlock4);
                        List<String> rawTags = new ArrayList<>();
                        tags.forEach((k, v) -> rawTags.add(k + "=" + v));

                        String sender = tags.getOrDefault("sender", "UNKNOWNXXX");
                        String receiver = tags.getOrDefault("receiver", "UNKNOWNXXX");
                        String ref = tags.getOrDefault("20", "REF-UNKNOWN");
                        String related = tags.getOrDefault("21", null);
                        LocalDate valueDate = parseDate(tags.get("32A"));
                        String ccy = parseCurrency(tags.get("32A"));
                        BigDecimal amount = parseAmount(tags.get("32A"));
                        String ordering = tags.getOrDefault("50K", tags.getOrDefault("50A", null));
                        String beneficiary = tags.getOrDefault("59", tags.getOrDefault("58A", null));
                        String remittance = tags.getOrDefault("70", null);

                        return new %CLS%Message(
                                sender, receiver, ref, related, valueDate,
                                ccy, amount, ordering, beneficiary, remittance,
                                rawTags, Instant.now());
                    }

                    private Map<String, String> tokenize(String block4) {
                        Map<String, String> out = new HashMap<>();
                        for (String line : block4.split("\\\\R")) {
                            String trimmed = line.trim();
                            if (trimmed.startsWith(":")) {
                                int closing = trimmed.indexOf(':', 1);
                                if (closing > 1) {
                                    String tag = trimmed.substring(1, closing);
                                    String value = trimmed.substring(closing + 1).trim();
                                    out.put(tag, value);
                                }
                            } else if (trimmed.startsWith("sender=")) {
                                out.put("sender", trimmed.substring(7));
                            } else if (trimmed.startsWith("receiver=")) {
                                out.put("receiver", trimmed.substring(9));
                            }
                        }
                        return out;
                    }

                    private LocalDate parseDate(String tag32A) {
                        if (tag32A == null || tag32A.length() < 6) return LocalDate.now();
                        try {
                            return LocalDate.parse(tag32A.substring(0, 6), DATE)
                                    .plusYears(2000 - 1900);
                        } catch (Exception e) {
                            return LocalDate.now();
                        }
                    }

                    private String parseCurrency(String tag32A) {
                        if (tag32A == null || tag32A.length() < 9) return "USD";
                        String candidate = tag32A.substring(6, 9);
                        try {
                            Currency.getInstance(candidate);
                            return candidate;
                        } catch (IllegalArgumentException e) {
                            return "USD";
                        }
                    }

                    private BigDecimal parseAmount(String tag32A) {
                        if (tag32A == null || tag32A.length() < 10) return BigDecimal.ZERO;
                        String amountPart = tag32A.substring(9).replace(",", ".");
                        try {
                            return new BigDecimal(amountPart);
                        } catch (NumberFormatException e) {
                            return BigDecimal.ZERO;
                        }
                    }
                }
                """
                .replace("%PKG%", pkg)
                .replace("%CLS%", cls)
                .replace("%MT%", s.mt());
    }

    static String validator(String pkg, String cls, MtSpec s) {
        return """
                package %PKG%;

                import java.math.BigDecimal;
                import java.time.LocalDate;
                import java.util.ArrayList;
                import java.util.List;
                import java.util.Objects;

                /**
                 * Business validation for parsed %MT% messages. Tag-level syntax
                 * is the parser's job; this class enforces content rules:
                 * required fields, value ranges, and cross-field invariants.
                 */
                public final class %CLS%Validator {

                    public record ValidationFinding(String code, String message, Severity severity) {
                        public enum Severity { ERROR, WARNING, INFO }
                    }

                    public record ValidationResult(List<ValidationFinding> findings) {
                        public boolean hasErrors() {
                            return findings.stream()
                                    .anyMatch(f -> f.severity() == ValidationFinding.Severity.ERROR);
                        }
                        public boolean isValid() { return !hasErrors(); }
                    }

                    public ValidationResult validate(%CLS%Message message) {
                        Objects.requireNonNull(message, "message");
                        List<ValidationFinding> findings = new ArrayList<>();

                        checkBicFormat(message.senderBic(), "senderBic", findings);
                        checkBicFormat(message.receiverBic(), "receiverBic", findings);
                        checkMessageReference(message.messageReference(), findings);

                        if (message.isPayment()) {
                            checkPaymentInvariants(message, findings);
                        }
                        if (%BENEFICIARY%) {
                            checkBeneficiaryPresent(message, findings);
                        }
                        if (%SETTLE%) {
                            checkSettlementViable(message, findings);
                        }
                        checkValueDateReasonable(message.valueDate(), findings);

                        return new ValidationResult(List.copyOf(findings));
                    }

                    private void checkBicFormat(String bic, String field, List<ValidationFinding> findings) {
                        if (bic == null || (bic.length() != 8 && bic.length() != 11)) {
                            findings.add(new ValidationFinding(
                                    "BIC_FORMAT", field + " must be 8 or 11 characters",
                                    ValidationFinding.Severity.ERROR));
                            return;
                        }
                        for (int i = 0; i < 4; i++) {
                            if (!Character.isLetter(bic.charAt(i))) {
                                findings.add(new ValidationFinding(
                                        "BIC_INSTITUTION", field + " institution code must be letters",
                                        ValidationFinding.Severity.ERROR));
                                return;
                            }
                        }
                    }

                    private void checkMessageReference(String ref, List<ValidationFinding> findings) {
                        if (ref == null || ref.isBlank()) {
                            findings.add(new ValidationFinding(
                                    "MISSING_REF", "Message reference (tag 20) is required",
                                    ValidationFinding.Severity.ERROR));
                        } else if (ref.length() > 16) {
                            findings.add(new ValidationFinding(
                                    "REF_TOO_LONG", "Message reference exceeds 16 chars",
                                    ValidationFinding.Severity.ERROR));
                        }
                    }

                    private void checkPaymentInvariants(%CLS%Message m, List<ValidationFinding> findings) {
                        BigDecimal amount = m.amount();
                        if (amount == null || amount.signum() <= 0) {
                            findings.add(new ValidationFinding(
                                    "NON_POSITIVE_AMOUNT", "Payment amount must be positive",
                                    ValidationFinding.Severity.ERROR));
                        }
                        if (m.currency() == null || m.currency().length() != 3) {
                            findings.add(new ValidationFinding(
                                    "INVALID_CURRENCY", "Currency must be ISO-4217 3-letter code",
                                    ValidationFinding.Severity.ERROR));
                        }
                    }

                    private void checkBeneficiaryPresent(%CLS%Message m, List<ValidationFinding> findings) {
                        if (m.beneficiary() == null || m.beneficiary().isBlank()) {
                            findings.add(new ValidationFinding(
                                    "MISSING_BENEFICIARY",
                                    "Beneficiary (tag 59) is required for %MT%",
                                    ValidationFinding.Severity.ERROR));
                        }
                    }

                    private void checkSettlementViable(%CLS%Message m, List<ValidationFinding> findings) {
                        if (m.valueDate() == null) {
                            findings.add(new ValidationFinding(
                                    "NO_VALUE_DATE", "Value date is required for settlement",
                                    ValidationFinding.Severity.ERROR));
                        }
                    }

                    private void checkValueDateReasonable(LocalDate valueDate, List<ValidationFinding> findings) {
                        if (valueDate == null) return;
                        LocalDate today = LocalDate.now();
                        if (valueDate.isBefore(today.minusDays(365))) {
                            findings.add(new ValidationFinding(
                                    "STALE_VALUE_DATE", "Value date more than a year ago",
                                    ValidationFinding.Severity.WARNING));
                        }
                        if (valueDate.isAfter(today.plusDays(90))) {
                            findings.add(new ValidationFinding(
                                    "FUTURE_VALUE_DATE", "Value date more than 90 days ahead",
                                    ValidationFinding.Severity.WARNING));
                        }
                    }
                }
                """
                .replace("%PKG%", pkg)
                .replace("%CLS%", cls)
                .replace("%MT%", s.mt())
                .replace("%BENEFICIARY%", String.valueOf(s.hasBeneficiary()))
                .replace("%SETTLE%", String.valueOf(s.requiresSettlement()));
    }

    static String routingRules(String pkg, String cls, MtSpec s) {
        return """
                package %PKG%;

                import java.util.Objects;
                import java.util.Set;

                /**
                 * Routing rules for %MT%. Determines which downstream handler
                 * queue gets the parsed message based on BIC and payment profile.
                 */
                public final class %CLS%RoutingRules {

                    public enum Destination {
                        CORRESPONDENT_BANK_GATEWAY,
                        INTRA_GROUP_LEDGER,
                        WIRE_PROCESSING_QUEUE,
                        FX_SETTLEMENT_QUEUE,
                        TRADE_FINANCE_QUEUE,
                        STATEMENT_POSTING_QUEUE,
                        DEAD_LETTER_QUEUE
                    }

                    private static final Set<String> INTRA_GROUP_PREFIXES = Set.of(
                            "OMNIUS33", "OMNIUS44", "OMNIGB22", "OMNIAU2S");

                    public Destination routeFor(%CLS%Message message) {
                        Objects.requireNonNull(message, "message");
                        String receiver = message.receiverBic();

                        if (isIntraGroup(receiver)) {
                            return Destination.INTRA_GROUP_LEDGER;
                        }

                        if (message.isStatement()) {
                            return Destination.STATEMENT_POSTING_QUEUE;
                        }

                        return switch ("%CAT%") {
                            case "1" -> message.isPayment()
                                    ? Destination.WIRE_PROCESSING_QUEUE
                                    : Destination.CORRESPONDENT_BANK_GATEWAY;
                            case "2" -> Destination.WIRE_PROCESSING_QUEUE;
                            case "3" -> Destination.FX_SETTLEMENT_QUEUE;
                            case "4" -> Destination.CORRESPONDENT_BANK_GATEWAY;
                            case "7" -> Destination.TRADE_FINANCE_QUEUE;
                            case "9" -> Destination.STATEMENT_POSTING_QUEUE;
                            default -> Destination.DEAD_LETTER_QUEUE;
                        };
                    }

                    public boolean isIntraGroup(String bic) {
                        if (bic == null) return false;
                        for (String prefix : INTRA_GROUP_PREFIXES) {
                            if (bic.startsWith(prefix.substring(0, 4))) return true;
                        }
                        return false;
                    }

                    public String priorityQueueOverride(%CLS%Message message) {
                        if (message.amount() != null
                                && message.amount().compareTo(java.math.BigDecimal.valueOf(10_000_000)) >= 0) {
                            return "HIGH_VALUE_REVIEW_QUEUE";
                        }
                        return null;
                    }
                }
                """
                .replace("%PKG%", pkg)
                .replace("%CLS%", cls)
                .replace("%MT%", s.mt())
                .replace("%CAT%", s.category());
    }

    static String handler(String pkg, String cls, MtSpec s) {
        return """
                package %PKG%;

                import java.time.Instant;
                import java.util.Objects;
                import java.util.concurrent.atomic.AtomicLong;

                /**
                 * End-to-end handler for SWIFT %MT%. Coordinates parse →
                 * validate → route → forward pipeline and records basic
                 * telemetry.
                 */
                public final class %CLS%Handler {

                    public record HandleResult(
                            boolean accepted,
                            String messageReference,
                            %CLS%RoutingRules.Destination destination,
                            String rejectionReason,
                            Instant processedAt
                    ) {}

                    private final %CLS%Parser parser = new %CLS%Parser();
                    private final %CLS%Validator validator = new %CLS%Validator();
                    private final %CLS%RoutingRules routing = new %CLS%RoutingRules();

                    private final AtomicLong processedCount = new AtomicLong();
                    private final AtomicLong acceptedCount = new AtomicLong();
                    private final AtomicLong rejectedCount = new AtomicLong();

                    public HandleResult handle(String rawBlock4) {
                        Objects.requireNonNull(rawBlock4, "rawBlock4");
                        processedCount.incrementAndGet();
                        %CLS%Message message;
                        try {
                            message = parser.parse(rawBlock4);
                        } catch (Exception e) {
                            rejectedCount.incrementAndGet();
                            return new HandleResult(false, null, null,
                                    "PARSE_ERROR: " + e.getMessage(), Instant.now());
                        }

                        var validation = validator.validate(message);
                        if (validation.hasErrors()) {
                            rejectedCount.incrementAndGet();
                            return new HandleResult(false, message.messageReference(), null,
                                    summarizeErrors(validation), Instant.now());
                        }

                        var dest = routing.routeFor(message);
                        acceptedCount.incrementAndGet();
                        return new HandleResult(true, message.messageReference(), dest, null, Instant.now());
                    }

                    public long processedCount() { return processedCount.get(); }
                    public long acceptedCount() { return acceptedCount.get(); }
                    public long rejectedCount() { return rejectedCount.get(); }

                    private String summarizeErrors(%CLS%Validator.ValidationResult vr) {
                        StringBuilder sb = new StringBuilder();
                        for (var f : vr.findings()) {
                            if (f.severity() == %CLS%Validator.ValidationFinding.Severity.ERROR) {
                                sb.append(f.code()).append(": ").append(f.message()).append("; ");
                            }
                        }
                        return sb.toString();
                    }
                }
                """
                .replace("%PKG%", pkg)
                .replace("%CLS%", cls)
                .replace("%MT%", s.mt());
    }

    static String handlerTest(String pkg, String cls, MtSpec s) {
        return """
                package %PKG%;

                import org.junit.jupiter.api.Test;
                import static org.assertj.core.api.Assertions.assertThat;

                class %CLS%HandlerTest {

                    private final %CLS%Handler handler = new %CLS%Handler();

                    @Test
                    void empty_block4_is_rejected() {
                        var result = handler.handle("");
                        assertThat(result.accepted()).isFalse();
                    }

                    @Test
                    void minimal_well_formed_message_returns_non_null_result() {
                        String block4 = \"""
                                sender=OMNIUS33XXX
                                receiver=CHASUS33XXX
                                :20:REF123
                                :32A:260416USD5000,00
                                :59:BENEFICIARY NAME
                                :70:INVOICE 4242
                                \""";
                        var result = handler.handle(block4);
                        assertThat(result).isNotNull();
                    }

                    @Test
                    void counters_increment_on_each_handle() {
                        handler.handle("");
                        handler.handle("");
                        assertThat(handler.processedCount()).isEqualTo(2);
                    }

                    @Test
                    void parser_accepts_basic_block() {
                        var parser = new %CLS%Parser();
                        String block4 = \"""
                                sender=OMNIUS33
                                receiver=CHASUS33
                                :20:REF-%MT%
                                :32A:260416USD1000,00
                                \""";
                        var msg = parser.parse(block4);
                        assertThat(msg.messageReference()).isEqualTo("REF-%MT%");
                    }

                    @Test
                    void routing_rules_produce_a_destination() {
                        var parser = new %CLS%Parser();
                        String block4 = \"""
                                sender=OMNIUS33
                                receiver=CHASUS33
                                :20:ROUTE1
                                :32A:260416USD5000,00
                                :59:BENEFICIARY
                                \""";
                        var msg = parser.parse(block4);
                        var rules = new %CLS%RoutingRules();
                        assertThat(rules.routeFor(msg)).isNotNull();
                    }

                    @Test
                    void intra_group_routing_detected() {
                        var rules = new %CLS%RoutingRules();
                        assertThat(rules.isIntraGroup("OMNIUS33XXX")).isTrue();
                        assertThat(rules.isIntraGroup("CHASUS33XXX")).isFalse();
                    }
                }
                """
                .replace("%PKG%", pkg)
                .replace("%CLS%", cls)
                .replace("%MT%", s.mt());
    }
